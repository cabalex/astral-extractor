function questAreaLookup(name) {
    // Quest formatting ("qXXXX" and "questXXXX")
    var formatted = name.replace("quest", "q").substr(0, 5);
    if (questToArea[formatted] != undefined) {
        return questToArea[formatted] + name.replace("quest", "q").substr(5);
    }
    return false;
}

function itemLookup(id) {
    if (items[parseInt(id)] != undefined) {
        return items[parseInt(id)];
    } else {
        return id;
    }
}

export function lookup(name) {
    // Looks up an enemy from database lists.
    if (!window.settings.useLookupTable || !name) { return name }
    if (name.includes('.') && name.includes('.dat')) name = name.split('.')[0];
    var formatted = name.toLowerCase().substr(0, 6);
    if (formatted.startsWith("q")) {
        // Quest formatting ("qXXXX" and "questXXXX")
        formatted = name.replace("quest", "q").substr(0, 5);
        if (questTable[formatted] != undefined) {
            return questTable[formatted] + name.replace("quest", "q").substr(5);
        }
        return name;
    }
    if (formatted.startsWith("p") && !(formatted.startsWith("pl"))) {
        // Quest formatting ("qXXXX" and "questXXXX")
        formatted = name.substr(0, 5);
        if (areaTable[formatted] != undefined) {
            return areaTable[formatted] + name.substr(5);
        }
        return name;
    }
    switch (formatted.substr(0, 2)) {
        case "em":
            if (em[formatted] != undefined) {
                return em[formatted] + name.substr(6);
            }
        case "pl":
            if (pl[formatted] != undefined) {
                return pl[formatted] + name.substr(6);
            }
        case "wp":
            if (wp[formatted] != undefined) {
                return wp[formatted] + name.substr(6);
            }
        case "bg":
            if (bg[formatted] != undefined) {
                return bg[formatted] + name.substr(6);
            }
        case "ba":
            if (ba[formatted] != undefined) {
                return ba[formatted] + name.substr(6);
            }
        default:
            return name;
    }
}